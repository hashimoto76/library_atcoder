"""
長さnの単調非減少な数列 a[0],...,a[n-1] と数kが与えられる.
a[i]>=kとなるような最小のiを求めよ.
存在しないときはnを出力せよ.

制約
1<=n<=10^6
0<=a[0]<=a[1]<=a[2]<=...<=a[n-1]<10^9
0<=k<=10^9
"""
#-------------------------------------------------------------------------------

n,k=map(int,input().split())
a=list(map(int,input().split()))
l,r=-1,n
while l+1<r:
    mid=(l+r)//2
    if a[mid]<=k:
        # k in (mid,r]
        l=mid
    else:
        # k in (l,mid]
        r=mid
print(r)