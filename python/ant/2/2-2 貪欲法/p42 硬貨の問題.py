"""
1円玉, 5円玉, 50円玉, 100円玉, 500円玉が
それぞれC_1, C_5, C_10, C_50, C_100, C_500枚ずつある.
できるだけ少ない枚数の硬貨でA円を支払いたいとき何枚の硬貨が必要か.
ただし, そのような支払い方が可能であると仮定する.
1<=C_1, C_5, C_10, C_50, C_100, C_500, A<=10^9
"""
#-------------------------------------------------------------------------------

c=list(map(int, input().split()))
a=int(input())
b=[1,5,10,50,100,500]
ans=0
t=0
for i in range(5,-1,-1):
    t=min(a//b[i],c[i])
    ans+=t
    a-=t*b[i]
print(ans)
